library("shiny")


## uživatelské rozhraní -------------------------------------------------------

ui <- fluidPage(
    
    # "nadpis" aplikace -------------------------------------------------------
    
    titlePanel("Ahoj světe!"),
    
    sidebarLayout(
        
        # ovládací prvky aplikace (vstupy; levý panel) ------------------------
        
        sidebarPanel(
            
            textInput(
                inputId = "my_input_text",
                label = "Sem vložte svůj text",
                placeholder = "Ahoj světe!"
            )
            
        ),
        
        ## výstupy; pravý panel -----------------------------------------------
        
        mainPanel(
            
            textOutput(outputId = "my_output_text")
            
        )
        
    )
    
)


## serverová strana -----------------------------------------------------------

server <- function(input, output){
    
    output$my_output_text <- renderText({
        
        input$my_input_text
        
    })
    
}


## zavolání aplikace ----------------------------------------------------------

shinyApp(ui, server)


